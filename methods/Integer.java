package com.sush.method;

public class Integer {
	
	private int age;
	private int duration;
	private int salary;
	public Integer() {
		
	}
	public Integer(int age, int duration) {
		super();
		this.age = age;
		this.duration = duration;
	}
	public Integer(int age, int duration, int salary) {
		super();
		this.age = age;
		this.duration = duration;
		this.salary = salary;
	}
	

}
