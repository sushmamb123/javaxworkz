package com.sush.method;

public class String {
	private String name;
	private String petname;
	private String favouriteFood;

	public String() {
	}

	public String(String name, String petname) {
		super();
		this.name = name;
		this.petname = petname;
	}

	public String(String name, String petname, String favouriteFood) {
		super();
		this.name = name;
		this.petname = petname;
		this.favouriteFood = favouriteFood;
	}

}
